function show_slected_videos_list(){
    
    for (let i = 0; i < menuItems.length; i++) {
        const item = menuItems[i];
        try {
            if (item["active"] == true){
                return handleClick(menuItems[i].text)
            }   
        } catch (error) {
            // console.log(error);
        }
    }    
}

